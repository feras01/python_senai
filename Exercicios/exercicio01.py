
# 1. Crie um programa que pergunte ao usuário um número de 1 a 3, E exiba o nome correspondente ao número (1: "um", 2: "dois", 3: "três").


numero =  int(input("Digite um valor: "))

if numero == 1:
    print('número um')
elif numero == 2:
    print('numero dois')
if numero == 3:
    print('número três')
